    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
		String from = jComboBox1.getSelectedItem().toString();
		String to = jComboBox2.getSelectedItem().toString();

		String fileNameFrom = from.substring(from.lastIndexOf("/"));
		String fileNameTo = to.substring(to.lastIndexOf("/"));

		File f = new File("resource/data/" + fileNameFrom);
		File t = new File("resource/data/" + fileNameTo);

		if (fileNameFrom.equals(fileNameTo)) {
			Dialog.info(I18N.get("コピー前とコピー後が同じです"));
			return;
		}
		if (Dialog.yesOrNo("FUZZY WORLD", DialogIcon.QUESTION, I18N.get("本当にコピーする")) == DialogOption.YES) {
			t.delete();
			try {
				Files.copy(f.toPath(), t.toPath(), StandardCopyOption.REPLACE_EXISTING);
			} catch (IOException ex) {
			}
			Dialog.info(I18N.get("コピー完了"));
		}
    }//GEN-LAST:event_jButton2ActionPerformed

    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
		String from = jComboBox1.getSelectedItem().toString();
		String fileNameFrom = from.substring(from.lastIndexOf("/"));
		File f = new File("resource/data/" + fileNameFrom);

		if (Dialog.yesOrNo("FUZZY WORLD", DialogIcon.QUESTION, fileNameFrom + "\n" + I18N.get("を本当に消す")) == DialogOption.YES) {
			try {
				f.delete();
				//初期化処理
				f.createNewFile();
				DBConnection.getInstance().open("file:./resource/data/" + fileNameFrom.replaceAll(".mv.db", ""), "sa", "adm");
				//Sound
				DBConnection.getInstance().execByFile("resource/data/sql/insertSound.sql");
				DBConnection.getInstance().execByFile("resource/data/sql/createTable.sql");
				//初期データ投入
				DBConnection.getInstance().execByFile("resource/data/sql/readData.sql");
				DBConnection.getInstance().close();

				Dialog.info(fileNameFrom + "\n" + I18N.get("削除完了"));
				updateQuest();
			} catch (IOException ex) {
			}
		}

    }//GEN-LAST:event_jButton1ActionPerformed

