	private void init() {
		setIconImage(new ImageIcon(getClass().getResource("icon.png")).getImage());

		Point center = GraphicsEnvironment.getLocalGraphicsEnvironment().getCenterPoint();
		center.x -= getWidth() / 2;
		center.y -= getHeight() / 2;
		setLocation(center);

		for (int i = 0; i < Const.Save.SAVE_DATA_NUM; i++) {
			int n = i + 1;
			String fileName = "resource/data/data" + n + ".mv.db";
			if (n == 1) {
				DBConnection.getInstance().open("file:./resource/data/data" + n, "sa", "adm");
				KResultSet rs1 = DBConnection
						.getInstance()
						.execDirect("select q.TITLE, q.DESC from S_CurrentQuest c left join Quest q on c.QuestID=q.QuestID where c.Questid = 'main'");
				if (rs1.isEmpty()) {
					jTextArea1.setText(I18N.get("新規"));
				} else {
					String title = rs1.row(0).get(0).get();
					String desc = rs1.row(0).get(1).get();
					jTextArea1.setText(title + "\n" + desc);
				}
				DBConnection.getInstance().close();
			}
			jComboBox1.addItem(fileName);
			jComboBox2.addItem(fileName);
		}
	}

